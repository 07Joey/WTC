#include <unistd.h>

int		main(int argc, char **argv)
{
	int		strlen;
	int		spc;
	int		i;

	i = 0;
	if (argc != 2)
	{
		write(1, "\n", 1);
	}
	while (argv[1][i] != "\0")
	{
		if (argv[1][i] == " " || argv[1][i] == "	")
				spc++;
		i++;
	}
	strlen = i;
	
}
